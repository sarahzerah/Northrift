                     <p class="excert">
                        Nandi County is Kenya’s athletic hub. As per the Nandi County government website it is a
                         home to the best celebrated athletes in the country and that is why it is termed the ‘Source of Champions.
                         It is also known for Agriculture and among the counties feeding the nation.
                     </p>
                     <p>
                        The North Rift Women in STEM conducted its first bootcamp at the ICT Centre in Nandi County, and was a three days Boot Camp on the 12th to 14th of March 2020. The Bootcamp was conducted by 4 volunteers, namely; Zipeta Nabwala, Timothy Owen, Sarah Chepwogen and Purity Kosgei; who were tech savvy youth pursuing careers in Computer Science and some being IT professionals. 
                     </p>
                     <p>
                           Northrift Women in STEM inclusion project partnered with ICT Centre,
                            Nandi County and VSO in having  PWDs women with hearing impairment as the main beneficiaries of the Bootcamp.
                             The first day entailed training on introduction to coding and Design Thinking Skills through
                              IdeaHacks Challenge. The Bootcamp commenced at the required time and both the volunteers and participants
                               were on time for the training. There was a brief introduction from the team and the agenda of the day 
                               was discussed for the participants. There were also two sign language interpreters for the training to
                                assist in interpretation for the participants since they were physically impaired (deaf). I lead the 
                                training of participants on coding for a period of two hours.
                            The participants were keen to learn and understand how to code and design basic websites. 
                  </p>
                     <p>
                        After the long session, the participants did a short activity to re-energize the 
                        participants then went for a short 15 minutes break in preparation for the next session.
                         They later had an IdeaHacks Challenge on Design Thinking Skills which was also led by
                          myself with the help from Timothy Owen and Purity Kosgei. The session entailed Ideation, 
                          Designing, Testing, and Pitching stages. The participants were divided into 3 groups for 
                          the session. The first stage was ideation which mainly involved the participants 
                          brainstorming together in coming up with new ideas and solutions within the shortest time
                           possible. The next stage involved creating prototypes for their designs for their ideas 
                           using Business Model Canvas. The participants used the available materials such as
                            writing materials, scissors and masking tapes and were required to be as creative as 
                            possible and try to make their ideas as simple as possible. The participants were then 
                            asked to seek more information regarding their ideas from their surrounding communities
                             as they break for the next day’s session.

                     </p>
                     <p>
                        The second day training involved Pitching stage of the IdeaHacks challenge.
                         The training got further intensive as the participants were asked to present their ideas in 
                         5 minutes. They were to receive feedback from both the volunteers and the rest of the participants 
                         in the form of comments and areas of improvement within 10 minutes for each group. The winning group
                          was around education, whereby their idea was about training corporates and institutions on sign language 
                          in partnership with the county government within the local community and countrywide. The first runner
                           up group had an idea on selling and supplying fermented milk within the local 
                        community on mobile and an affordable fee.
                     </p>
                     <p>
                        The third group had an idea on offering health services to people on a door to door basis, 
                        giving the health care contacts to people for further medical advice. The participants had a 
                        chance to learn presentation skills and how to pitch their ideas in the right way. After the 
                        pitching session, there was an activity that involved team building for a few minutes then later 
                        went for a break. The session was then winded up with Online Safety training from Sarah Chepwogen. 
                        The participants were trained on becoming better internet users.
                     </p>
                     <p>
                        The final day of the Bootcamp was issuing certificates to the participants. 
                        The day commenced with a speech from the Chief Officer Jonathan Misoi of ICT Nandi County.
                         He then proceeded in issuing North Rift Women in STEM certificates to the participants,
                          appreciating them for taking part in the Bootcamp training. One of the participants was 
                          also given a chance to give a vote of thanks on behalf of the fellow participants. 
                          The feedback was positive as the participants confirmed that they really learnt a lot
                           and had wanted to have more of such training often. The sign language interpreters got 
                           a chance to share their opinions and ideas as to how they can work together with 
                           the county in reaching out to more PWDs in the community. 

                     </p>
